<h3>Was ist das Ziel eures Projekts?</h3>
<p>Es soll aufgezeigt werden, wie eine digitale Schnittstelle aussehen und funktionieren k&ouml;nnte, die im Bereich der Kinder- und Jugendhilfe niederschwellig eine kurzfristige Beratung durch professionelle Ansprechpersonen als &Uuml;berbr&uuml;ckung erm&ouml;glicht.<br />
